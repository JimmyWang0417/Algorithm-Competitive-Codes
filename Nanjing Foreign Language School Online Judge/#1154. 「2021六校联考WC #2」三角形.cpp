constexpr int N = 10005;
constexpr ll base = 1e17;
int n;
ll a[N], b[N];
bool dp[67][3];
int vis[67][3], vistime;
inline bool dfs(int pos, int qwq, ll l, ll r, ll v, bool limitl, bool limitr)
{
    if (pos == -1)
        return !qwq;
    if (!limitl && !limitr && vis[pos][qwq] == vistime)
        return dp[pos][qwq];
    bool res = 0;
    int minn = (int)(!limitl ? 0 : ((l >> pos) & 1)), maxx = (!limitr ? 1 : ((r >> pos) & 1));
    for (int i = minn; i <= maxx && i <= ((v >> pos) & 1); ++i)
        res ^= dfs(pos - 1, (int)((qwq + (1ll << pos) * i) % 3), l, r, v, limitl && i == minn, limitr && i == maxx);
    if (!limitl && !limitr)
    {
        dp[pos][qwq] = res;
        vis[pos][qwq] = vistime;
    }
    return res;
}
inline bool calc(ll l, ll r, int k)
{
    bool res = 0;
    for (int i = 1; i <= n; ++i)
    {
        ll x = max(l - a[i], 0ll), y = r - a[i];
        if (x > y)
            continue;
        ++vistime;
        res ^= dfs(60, (int)(((a[i] - k) % 3 + 3) % 3), x, y, b[i], 1, 1);
    }
    return res;
}
inline bool check(ll mid)
{
    bool res = 0;
    for (int i = 1; i <= n; ++i)
        res ^= (a[i] <= mid && ((mid - a[i]) | b[i]) == b[i]);
    return res;
}
