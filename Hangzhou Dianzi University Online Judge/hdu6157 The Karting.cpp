using namespace std;
constexpr int N = 105;
int n, m, a[N];
int dp[N][N];
signed main()
{
    while (scanf("%d%d", &n, &m) != EOF)
    {
        read(a[0]);
        for (int i = 2; i <= n; ++i)
        {
            read(a[i]);
            a[i] += a[i - 1];
        }
        memset(dp, ~0x3f, sizeof(dp));
        dp[0][0] = 0;
        for (int i = 1; i <= n; ++i)
        {
            for (int j = m; j >= 1; --j)
                for (int k = j; k >= 0; --k)
                {
                    ckmax(dp[j][k], dp[j - 1][k]);
                    if (k)
                        ckmax(dp[j][k], dp[j - 1][k - 1] + a[0] - 2 * a[i]);
                    ckmax(dp[j][k], dp[j - 1][k + 1] + a[0] + 2 * a[i]);
                }
        }
        write(dp[m][0], '\n');
    }
#ifdef FAST_OUT
    IO::OUTPUT::flush();
#endif
    return 0;
}