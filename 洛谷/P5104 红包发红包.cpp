using namespace std;
const int mod = 1e9 + 7, inv2 = (mod + 1) >> 1;
inline ll quickpow(ll a, ll b)
{
    ll res = 1;
    while (b)
    {
        if (b & 1)
            (res *= a) %= mod;
        (a *= a) %= mod;
        b >>= 1;
    }
    return res;
}
