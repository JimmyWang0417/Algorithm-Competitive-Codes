int top[N];
int fa[N], dep[N];
int size[N], son[N];
int dfn[N], rk[N], dfstime;
inline void dfs1(int u, int _fa)
{
    dep[u] = dep[fa[u] = _fa] + 1;
    size[u] = 1;
    for (int i = head[u]; i; i = edge[i].next)
    {
        int v = edge[i].to;
        if (v == _fa)
            continue;
        dfs1(v, u);
        size[u] += size[v];
        if (size[son[u]] < size[v])
            son[u] = v;
    }
}
inline void dfs2(int u, int topf)
{
    top[u] = topf;
    rk[dfn[u] = ++dfstime] = u;
    if (son[u])
        dfs2(son[u], topf);
    for (int i = head[u]; i; i = edge[i].next)
    {
        int v = edge[i].to;
        if (dfn[v])
            continue;
        dfs2(v, v);
    }
}
struct Tree
{
    ll ans;
    ll size;
    int tag;
    inline void update(int val)
    {
        ans += size * val;
        tag += val;
    }
} tree[N * 4];
#define lc (rt << 1)
#define rc (rt << 1 | 1)
inline void pushup(int rt)
{
    tree[rt].ans = tree[lc].ans + tree[rc].ans;
}
inline void pushdown(int rt)
{
    if (tree[rt].tag)
    {
        tree[lc].update(tree[rt].tag);
        tree[rc].update(tree[rt].tag);
        tree[rt].tag = 0;
    }
}
inline void build(int rt, int l, int r)
{
    if (l == r)
    {
        tree[rt].size = dep[rk[l]] * 2 - 1;
        return;
    }
    int mid = (l + r) >> 1;
    build(lc, l, mid);
    build(rc, mid + 1, r);
    tree[rt].size = tree[lc].size + tree[rc].size;
}
inline void update(int rt, int l, int r, int x, int y)
{
    if (r < x || l > y)
        return;
    if (x <= l && r <= y)
        return tree[rt].update(1);
    int mid = (l + r) >> 1;
    pushdown(rt);
    update(lc, l, mid, x, y);
    update(rc, mid + 1, r, x, y);
    pushup(rt);
}
inline ll query(int rt, int l, int r, int x, int y)
{
    if (r < x || l > y)
        return 0;
    if (x <= l && r <= y)
        return tree[rt].ans;
    int mid = (l + r) >> 1;
    pushdown(rt);
    return query(lc, l, mid, x, y) + query(rc, mid + 1, r, x, y);
}
inline void update(int x)
{
    while (top[x] != 1)
    {
        update(1, 1, n, dfn[top[x]], dfn[x]);
        x = fa[top[x]];
    }
    update(1, 1, n, 1, dfn[x]);
}
inline ll query(int x)
{
    ll res = 0;
    while (top[x] != 1)
    {
        res += query(1, 1, n, dfn[top[x]], dfn[x]);
        x = fa[top[x]];
    }
    res += query(1, 1, n, 1, dfn[x]);
    return res;
}
