node C[N][N];
node dp[N][N][N];
node f[N], g[N];
node sum[N][N];
node olddp[N][N];
list<int> have[N];
inline void merge(int u, int v)
{
    for (int i = 1; i <= sze[u]; ++i)
        for (int j = 1; j <= n; ++j)
            sum[i][j] = sum[i - 1][j] + sum[i][j - 1] - sum[i - 1][j - 1] + (olddp[i][j] = dp[u][i][j]);
    f[u] = (f[u] * g[v] + f[v] * g[u]) * C[sze[u] + sze[v] - 1][sze[v]];
    memset(dp[u], 0, sizeof(dp[u]));
    for (int p = 1; p <= sze[v]; ++p)
        for (int i : have[v])
            for (int c = 0; c < sze[u]; ++c)
                if (dp[v][p][i].data())
                {
                    auto times = dp[v][p][i] * C[c + p - 1][c] * C[sze[u] + sze[v] - c - 1 - p][sze[u] - c - 1];
                    dp[u][p + c][i] += times * g[u];
                    f[u] += times * (sum[c][n] - sum[c][i] + sum[sze[u]][i] - sum[c][i]);
                }
    for (int p = 1; p <= sze[u]; ++p)
        for (int i : have[u])
            if (olddp[p][i].data())
                for (int c = 0; c <= sze[v]; ++c)
                {
                    if (p != sze[u] || sze[v] <= c)
                    {
                        auto times = C[c + p - 1][c] * olddp[p][i];
                        if (sze[v] > c && sze[u] > p)
                            times *= C[sze[u] + sze[v] - c - p - 1][sze[v] - c];
                        dp[u][p + c][i] += times * g[v];
                    }
                }
    g[u] = g[u] * g[v] * C[sze[u] + sze[v] - 1][sze[v]];
    sze[u] += sze[v];
    have[u].splice(have[u].end(), have[v]);
}
inline void dfs(int u, int _fa)
{
    dp[u][1][u] = g[u] = sze[u] = 1;
    have[u].push_back(u);
    for (int i = head[u]; i; i = edge[i].next)
    {
        int v = edge[i].to;
        if (v == _fa)
            continue;
        dfs(v, u);
        merge(u, v);
    }
}
