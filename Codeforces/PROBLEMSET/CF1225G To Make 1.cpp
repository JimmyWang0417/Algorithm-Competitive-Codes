constexpr int N = (1 << 16) + 5;
constexpr int M = 2001;
int n, m, k;
int a[M], b[M];
int Log[N];
bitset<M> dp[N];
signed main()
{
    read(n, k);
    for (int i = 0; i < n; ++i)
    {
        read(a[i]);
        Log[1 << i] = i;
        m += a[i];
    }
    dp[0][0] = 1;
    for (int S = 0; S < (1 << n); ++S)
    {
        for (int i = m / k; i >= 1; --i)
            if (dp[S][i * k])
                dp[S][i] = true;
        for (int i = ((1 << n) - 1) ^ S; i; i ^= lowbit(i))
            dp[S | lowbit(i)] |= dp[S] << a[Log[lowbit(i)]];
    }
    if (!dp[(1 << n) - 1][1])
        write("NO\n");
    else
    {
        write("YES\n");
        for (int i = (1 << n) - 1, j = 1; i;)
        {
            for (; j * k <= m && dp[i][j * k]; j *= k)
                for (int S = i; S; S ^= lowbit(S))
                    ++b[Log[lowbit(S)]];
            for (int S = i; S; S ^= lowbit(S))
            {
                int l = Log[lowbit(S)];
                if (j >= a[l] && dp[i ^ lowbit(S)][j - a[l]])
                {
                    i ^= lowbit(S), j -= a[l];
                    break;
                }
            }
        }
        priority_queue<pair<int, int>> q;
        for (int i = 0; i < n; ++i)
            q.emplace(b[i], a[i]);
        while (q.size() > 1)
        {
            auto [u, v] = q.top();
            q.pop();
            auto [x, y] = q.top();
            q.pop();
            write(v, ' ', y, '\n');
            v += y;
            while (v % k == 0)
            {
                --u;
                v /= k;
            }
            q.emplace(u, v);
        }
    }
#ifdef FAST_OUT
    IO::OUTPUT::flush();
#endif
    return 0;
}