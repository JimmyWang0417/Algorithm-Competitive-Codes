constexpr int N = 1e6 + 5;
int limit;
int fa[N], gcd[N], all[N];
ll dis[N];
inline int find(int x)
{
    if (!fa[x])
        return x;
    int _fa = fa[x];
    fa[x] = find(fa[x]);
    dis[x] += dis[_fa];
    return fa[x];
}
inline void unionn(int x, int y, int c)
{
    int fx = find(x), fy = find(y);
    if (fx != fy)
    {
        fa[fy] = fx;
        dis[fy] = dis[x] + c - dis[y];
        gcd[fx] = __gcd(__gcd(gcd[fx], gcd[fy]), c);
        all[fx] |= all[fy];
    }
    else
    {
        gcd[fx] = __gcd(gcd[fx], c);
        int pos = __builtin_ctzll(dis[x] + c - dis[y]);
        if (pos <= limit && !((all[fx] >> pos) & 1))
            all[fx] |= 1 << pos;
    }
}
inline void exgcd(int a, int b, int &x, int &y)
{
    if (!b)
    {
        x = 1;
        y = 0;
        return;
    }
    exgcd(b, a % b, y, x);
    y -= a / b * x;
}
inline int inv(int a, int mod)
{
    int x, y;
    exgcd(a, mod, x, y);
    return (x % mod + mod) % mod;
}
inline int solve(int n, int a, int b, int mod)
{
    int g = std::__gcd(a, mod);
    if (b % g)
        return 0;
    a /= g;
    b /= g;
    mod /= g;
    b = (int)((ll)b * inv(a, mod) % mod);
    return n / mod + (b <= n % mod);
}
